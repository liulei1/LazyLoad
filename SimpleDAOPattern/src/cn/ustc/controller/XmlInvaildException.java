package cn.ustc.controller;

public class XmlInvaildException extends Exception {

	public XmlInvaildException() {
		super();
	}

	public XmlInvaildException(String message, Throwable cause, boolean enableSuppression, boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
	}

	public XmlInvaildException(String message, Throwable cause) {
		super(message, cause);
	}

	public XmlInvaildException(String message) {
		super(message);
	}

	public XmlInvaildException(Throwable cause) {
		super(cause);
	}

}
